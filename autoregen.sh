#!/bin/sh

rm -rf autom4te.cache/

################################################################################
echo "-- Running aclocal"
################################################################################
aclocal -I config

################################################################################
echo "-- Running autoconf"
################################################################################
autoconf -f

################################################################################
echo "-- Running autoheader"
################################################################################
autoheader

################################################################################
echo "-- Running automake"
################################################################################
automake -a

################################################################################
echo "-- Generating autotool files for lib/gdbmi"
################################################################################
cd ./lib/gdbmi
./autoregen.sh
